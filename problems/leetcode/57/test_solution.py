import pytest

from solution import Solution

@pytest.mark.solution
@pytest.mark.parametrize(
    ('test_input', 'expected'),
    [
        (([[1,3],[6,9]], [2,5]), [[1,5],[6,9]]),
        (([[1,3],[6,9]], [1,5]), [[1,5],[6,9]]),
        (([[1,3],[6,9]], [0,5]), [[0,5],[6,9]]),
        (([[1,3],[6,9]], [0,9]), [[0,9]]),
        (([[1,3],[6,9]], [1,9]), [[1,9]]),
        (([[1,3],[6,9]], [3,9]), [[1,9]]),
        (([[1,3],[6,9]], [3,10]), [[1,10]]),
        (([[1,3],[6,9]], [3,5]), [[1,5],[6,9]]),
        (([], [1,5]), [[1,5]]),
        (([[1,5]], [6,8]), [[1,5], [6,8]]),
        (([[6,8]], [0,0]), [[0,0], [6,8]]),
        (([[2,5],[6,7],[8,9]], [0,1]), [[0,1],[2,5],[6,7],[8,9]]),
    ]
)
def testSolution(test_input, expected):
    assert Solution().insert(*test_input) == expected
